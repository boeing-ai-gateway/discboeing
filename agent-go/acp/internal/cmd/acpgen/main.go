package main

import (
	"bytes"
	"encoding/json"
	"flag"
	"fmt"
	"go/format"
	"os"
	"sort"
	"strings"
	"unicode"
)

const rawMessageType = "json.RawMessage"

type schemaFile struct {
	Defs map[string]json.RawMessage `json:"$defs"`
}

type schema struct {
	AdditionalProperties any               `json:"additionalProperties"`
	AllOf                []schema          `json:"allOf"`
	AnyOf                []schema          `json:"anyOf"`
	Const                any               `json:"const"`
	Default              any               `json:"default"`
	Description          string            `json:"description"`
	Discriminator        *discriminator    `json:"discriminator"`
	Enum                 []any             `json:"enum"`
	Format               string            `json:"format"`
	Items                *schema           `json:"items"`
	Maximum              *int              `json:"maximum"`
	Minimum              *int              `json:"minimum"`
	OneOf                []schema          `json:"oneOf"`
	Properties           map[string]schema `json:"properties"`
	Ref                  string            `json:"$ref"`
	Required             []string          `json:"required"`
	Title                string            `json:"title"`
	Type                 any               `json:"type"`
	XDocsIgnore          bool              `json:"x-docs-ignore"`
	XMethod              string            `json:"x-method"`
	XSide                string            `json:"x-side"`
}

type discriminator struct {
	PropertyName string `json:"propertyName"`
}

func (s *schema) UnmarshalJSON(data []byte) error {
	var fields map[string]json.RawMessage
	if err := json.Unmarshal(data, &fields); err != nil {
		return err
	}
	allowed := map[string]bool{
		"$ref": true, "additionalProperties": true, "allOf": true, "anyOf": true,
		"const": true, "default": true, "description": true, "discriminator": true,
		"enum": true, "format": true, "items": true, "maximum": true,
		"minimum": true, "oneOf": true, "properties": true, "required": true,
		"title": true, "type": true, "x-docs-ignore": true, "x-method": true,
		"x-side": true,
	}
	for name := range fields {
		if !allowed[name] {
			return fmt.Errorf("unsupported schema field %q", name)
		}
	}
	type plain schema
	var out plain
	if err := json.Unmarshal(data, &out); err != nil {
		return err
	}
	*s = schema(out)
	return nil
}

type generator struct {
	defs      map[string]json.RawMessage
	parsed    map[string]schema
	reachable map[string]bool
}

func main() {
	schemaPath := flag.String("schema", "", "path to ACP schema.json")
	outPath := flag.String("out", "", "path to generated Go file")
	flag.Parse()
	if *schemaPath == "" || *outPath == "" {
		panic("schema and out flags are required")
	}

	data, err := os.ReadFile(*schemaPath)
	if err != nil {
		panic(err)
	}
	var file schemaFile
	if err := json.Unmarshal(data, &file); err != nil {
		panic(err)
	}
	if len(file.Defs) == 0 {
		panic("schema has no $defs")
	}

	g := generator{defs: file.Defs, parsed: map[string]schema{}, reachable: map[string]bool{}}
	for name := range file.Defs {
		def := g.mustDef(name)
		if def.XMethod != "" {
			g.collectDef(name)
		}
	}
	data = g.generate()
	formatted, err := format.Source(data)
	if err != nil {
		panic(fmt.Errorf("format generated source: %w\n%s", err, data))
	}
	if err := os.WriteFile(*outPath, formatted, 0o644); err != nil {
		panic(err)
	}
}

func (g *generator) collectDef(name string) {
	if g.reachable[name] {
		return
	}
	def := g.mustDef(name)
	g.reachable[name] = true
	g.collectSchema(def)
}

func (g *generator) mustDef(name string) schema {
	if def, ok := g.parsed[name]; ok {
		return def
	}
	raw, ok := g.defs[name]
	if !ok {
		panicf("unknown $ref %q", name)
	}
	var def schema
	if err := json.Unmarshal(raw, &def); err != nil {
		panic(fmt.Errorf("parse $defs/%s: %w", name, err))
	}
	g.parsed[name] = def
	return def
}

func (g *generator) collectSchema(s schema) {
	if s.Ref != "" {
		g.collectDef(refSchemaName(s.Ref))
	}
	for _, part := range s.AllOf {
		g.collectSchema(part)
	}
	for _, part := range s.AnyOf {
		g.collectSchema(part)
	}
	for _, part := range s.OneOf {
		g.collectSchema(part)
	}
	if s.Items != nil {
		g.collectSchema(*s.Items)
	}
	for _, prop := range s.Properties {
		g.collectSchema(prop)
	}
}

func (g *generator) generate() []byte {
	var buf bytes.Buffer
	buf.WriteString("// Code generated by acpgen from schema/schema.json; DO NOT EDIT.\n\n")
	buf.WriteString("package protocol\n\n")
	buf.WriteString("import \"encoding/json\"\n\n")
	g.writeDiscriminatedUnionHelpers(&buf)

	names := make([]string, 0, len(g.reachable))
	for name := range g.reachable {
		names = append(names, name)
	}
	sort.Strings(names)

	for _, name := range names {
		def := g.mustDef(name)
		goName := goTypeName(name)
		if def.XMethod != "" {
			fmt.Fprintf(&buf, "const %sMethod = %q\n\n", goName, def.XMethod)
		}
		g.writeDef(&buf, goName, def)
	}
	return buf.Bytes()
}

func (g *generator) writeDiscriminatedUnionHelpers(buf *bytes.Buffer) {
	buf.WriteString("func marshalDiscriminatedUnion(propertyName, propertyValue string, value any) json.RawMessage {\n")
	buf.WriteString("\tdata, err := json.Marshal(value)\n")
	buf.WriteString("\tif err != nil {\n\t\treturn nil\n\t}\n")
	buf.WriteString("\tvar fields map[string]json.RawMessage\n")
	buf.WriteString("\tif err := json.Unmarshal(data, &fields); err != nil || fields == nil {\n\t\tfields = map[string]json.RawMessage{}\n\t}\n")
	buf.WriteString("\tdiscriminator, err := json.Marshal(propertyValue)\n")
	buf.WriteString("\tif err != nil {\n\t\treturn nil\n\t}\n")
	buf.WriteString("\tfields[propertyName] = discriminator\n")
	buf.WriteString("\tdata, err = json.Marshal(fields)\n")
	buf.WriteString("\tif err != nil {\n\t\treturn nil\n\t}\n")
	buf.WriteString("\treturn data\n")
	buf.WriteString("}\n\n")
}

func (g *generator) writeDef(buf *bytes.Buffer, name string, def schema) {
	if len(def.OneOf) > 0 {
		g.writeUnionDef(buf, name, def)
		return
	}
	if len(def.Enum) > 0 {
		g.writeStringConstDef(buf, name, def.Enum)
		return
	}
	if len(def.AnyOf) > 0 {
		if g.writeAnyOfDef(buf, name, def) {
			return
		}
		typ, nullable := g.goType(def, true)
		if nullable {
			panicf("top-level nullable definition %s", name)
		}
		fmt.Fprintf(buf, "type %s = %s\n\n", name, typ)
		return
	}
	if len(def.AllOf) > 0 || def.Ref != "" {
		typ, nullable := g.goType(def, true)
		if nullable {
			panicf("top-level nullable definition %s", name)
		}
		fmt.Fprintf(buf, "type %s = %s\n\n", name, typ)
		return
	}
	switch schemaType(def) {
	case "object":
		g.writeStruct(buf, name, def)
	case "integer":
		if def.Format == "uint16" && def.Minimum != nil && *def.Minimum == 0 && def.Maximum != nil && *def.Maximum == 65535 {
			fmt.Fprintf(buf, "type %s uint16\n\n", name)
			return
		}
		if def.Format == "int32" {
			fmt.Fprintf(buf, "type %s int32\n\n", name)
			return
		}
		panicf("unsupported integer definition %s", name)
	case "string":
		fmt.Fprintf(buf, "type %s string\n\n", name)
	default:
		panicf("unsupported definition %s type %q", name, schemaType(def))
	}
}

func (g *generator) writeUnionDef(buf *bytes.Buffer, name string, def schema) {
	if allConstStrings(def.OneOf) {
		g.writeStringConstDef(buf, name, constValues(def.OneOf))
		return
	}
	if def.Discriminator != nil {
		g.writeRawUnionWrapper(buf, name)
		g.writeDiscriminatedUnion(buf, name, def)
		return
	}
	fmt.Fprintf(buf, "type %s %s\n\n", name, rawMessageType)
	g.writeRawMessageMethods(buf, name)
}

func (g *generator) writeRawUnionWrapper(buf *bytes.Buffer, name string) {
	fmt.Fprintf(buf, "type %s struct {\n\tdata %s\n}\n\n", name, rawMessageType)
	fmt.Fprintf(buf, "func New%sRaw(data %s) %s {\n", name, rawMessageType, name)
	fmt.Fprintf(buf, "\treturn %s{data: append(%s(nil), data...)}\n", name, rawMessageType)
	fmt.Fprintf(buf, "}\n\n")
	fmt.Fprintf(buf, "func (m *%s) UnmarshalJSON(data []byte) error {\n", name)
	fmt.Fprintf(buf, "\tm.data = append(m.data[:0], data...)\n")
	fmt.Fprintf(buf, "\treturn nil\n")
	fmt.Fprintf(buf, "}\n\n")
	fmt.Fprintf(buf, "func (m %s) MarshalJSON() ([]byte, error) {\n", name)
	fmt.Fprintf(buf, "\tif len(m.data) == 0 {\n\t\treturn []byte(\"null\"), nil\n\t}\n")
	fmt.Fprintf(buf, "\treturn m.data, nil\n")
	fmt.Fprintf(buf, "}\n\n")
	fmt.Fprintf(buf, "func (m %s) Raw() %s {\n", name, rawMessageType)
	fmt.Fprintf(buf, "\treturn append(%s(nil), m.data...)\n", rawMessageType)
	fmt.Fprintf(buf, "}\n\n")
}

func (g *generator) writeRawMessageMethods(buf *bytes.Buffer, name string) {
	fmt.Fprintf(buf, "func (m *%s) UnmarshalJSON(data []byte) error {\n", name)
	fmt.Fprintf(buf, "\t*m = append((*m)[0:0], data...)\n")
	fmt.Fprintf(buf, "\treturn nil\n")
	fmt.Fprintf(buf, "}\n\n")
	fmt.Fprintf(buf, "func (m %s) MarshalJSON() ([]byte, error) {\n", name)
	fmt.Fprintf(buf, "\tif m == nil {\n\t\treturn []byte(\"null\"), nil\n\t}\n")
	fmt.Fprintf(buf, "\treturn m, nil\n")
	fmt.Fprintf(buf, "}\n\n")
}

func (g *generator) writeDiscriminatedUnion(buf *bytes.Buffer, name string, def schema) {
	prop := def.Discriminator.PropertyName
	if strings.TrimSpace(prop) == "" {
		panicf("discriminator for %s has empty propertyName", name)
	}
	iface := name + "Variant"
	marker := "is" + name + "Variant"
	discriminatorField := fieldName(prop)

	fmt.Fprintf(buf, "type %s interface {\n\t%s()\n\t%s() %s\n}\n\n", iface, marker, name, name)
	for _, variant := range def.OneOf {
		constValue, ok := discriminatorConst(variant, prop)
		if !ok {
			panicf("discriminated union %s variant missing const property %q", name, prop)
		}
		variantName := name + fieldName(constValue)
		constName := variantName + discriminatorField
		fmt.Fprintf(buf, "const %s = %q\n\n", constName, constValue)
		refName := ""
		if len(variant.AllOf) > 0 {
			if len(variant.AllOf) != 1 || variant.AllOf[0].Ref == "" {
				panicf("discriminated union %s variant %q has unsupported allOf", name, constValue)
			}
			refName = refGoName(variant.AllOf[0].Ref)
		}
		if schemaType(variant) != "object" {
			panicf("discriminated union %s variant %q has unsupported type %q", name, constValue, schemaType(variant))
		}
		fmt.Fprintf(buf, "type %s struct {\n", variantName)
		if refName != "" {
			fmt.Fprintf(buf, "\t%s\n", refName)
		}
		fmt.Fprintf(buf, "}\n\n")
		fmt.Fprintf(buf, "func (%s) %s() {}\n\n", variantName, marker)
		fmt.Fprintf(buf, "func (v %s) %s() %s {\n", variantName, name, name)
		fmt.Fprintf(buf, "\treturn %s{data: marshalDiscriminatedUnion(%q, %s, v)}\n", name, prop, constName)
		fmt.Fprintf(buf, "}\n\n")
	}

	unknownName := "Unknown" + name
	fmt.Fprintf(buf, "type %s struct {\n\tDiscriminator string\n\tRaw %s\n}\n\n", unknownName, rawMessageType)
	fmt.Fprintf(buf, "func (%s) %s() {}\n\n", unknownName, marker)
	fmt.Fprintf(buf, "func (v %s) %s() %s {\n", unknownName, name, name)
	fmt.Fprintf(buf, "\treturn %s{data: append(%s(nil), v.Raw...)}\n", name, rawMessageType)
	fmt.Fprintf(buf, "}\n\n")
	fmt.Fprintf(buf, "func (m %s) Variant() %s {\n", name, iface)
	fmt.Fprintf(buf, "\tvariant, err := Decode%s(m.Raw())\n", name)
	fmt.Fprintf(buf, "\tif err != nil {\n\t\treturn %s{Raw: m.Raw()}\n\t}\n", unknownName)
	fmt.Fprintf(buf, "\treturn variant\n")
	fmt.Fprintf(buf, "}\n\n")
	fmt.Fprintf(buf, "func Decode%s(data %s) (%s, error) {\n", name, rawMessageType, iface)
	fmt.Fprintf(buf, "\tvar header struct {\n\t\t%s string `json:%q`\n\t}\n", discriminatorField, prop)
	fmt.Fprintf(buf, "\tif err := json.Unmarshal(data, &header); err != nil {\n\t\treturn nil, err\n\t}\n")
	fmt.Fprintf(buf, "\tswitch header.%s {\n", discriminatorField)
	for _, variant := range def.OneOf {
		constValue, _ := discriminatorConst(variant, prop)
		variantName := name + fieldName(constValue)
		constName := variantName + discriminatorField
		fmt.Fprintf(buf, "\tcase %s:\n", constName)
		fmt.Fprintf(buf, "\t\tvar value %s\n", variantName)
		fmt.Fprintf(buf, "\t\tif err := json.Unmarshal(data, &value); err != nil {\n\t\t\treturn nil, err\n\t\t}\n")
		fmt.Fprintf(buf, "\t\treturn value, nil\n")
	}
	fmt.Fprintf(buf, "\tdefault:\n")
	fmt.Fprintf(buf, "\t\treturn %s{Discriminator: header.%s, Raw: append(%s(nil), data...)}, nil\n", unknownName, discriminatorField, rawMessageType)
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "}\n\n")
}

func (g *generator) writeAnyOfDef(buf *bytes.Buffer, name string, def schema) bool {
	if len(def.AnyOf) > 2 && allConstSamePrimitive(def.AnyOf) {
		base := constPrimitiveType(def.AnyOf)
		fmt.Fprintf(buf, "type %s %s\n\n", name, base)
		g.writeConstBlock(buf, name, def.AnyOf)
		return true
	}
	if len(def.AnyOf) > 2 {
		fmt.Fprintf(buf, "type %s %s\n\n", name, rawMessageType)
		return true
	}
	return false
}

func (g *generator) writeStringConstDef(buf *bytes.Buffer, name string, values []any) {
	fmt.Fprintf(buf, "type %s string\n\n", name)
	items := make([]schema, 0, len(values))
	for _, value := range values {
		items = append(items, schema{Const: value, Type: "string"})
	}
	g.writeConstBlock(buf, name, items)
}

func (g *generator) writeConstBlock(buf *bytes.Buffer, name string, values []schema) {
	if len(values) == 0 {
		return
	}
	buf.WriteString("const (\n")
	for _, item := range values {
		if item.Const == nil {
			continue
		}
		fmt.Fprintf(buf, "\t%s%s %s = %s\n", name, constName(item.Const), name, constLiteral(item.Const))
	}
	buf.WriteString(")\n\n")
}

func (g *generator) writeStruct(buf *bytes.Buffer, name string, def schema) {
	if len(def.Properties) == 0 {
		fmt.Fprintf(buf, "type %s struct {\n}\n\n", name)
		return
	}
	required := map[string]bool{}
	for _, prop := range def.Required {
		required[prop] = true
	}

	props := make([]string, 0, len(def.Properties))
	for prop := range def.Properties {
		props = append(props, prop)
	}
	sort.Strings(props)

	fmt.Fprintf(buf, "type %s struct {\n", name)
	for _, prop := range props {
		propSchema := def.Properties[prop]
		typ, nullable := g.goType(propSchema, false)
		field := fieldName(prop)
		tag := prop
		if !required[prop] || nullable {
			omitOption := "omitempty"
			if !nullable && g.omitZeroField(propSchema) {
				omitOption = "omitzero"
			}
			tag += "," + omitOption
		}
		fmt.Fprintf(buf, "\t%s %s `json:%q`\n", field, typ, tag)
	}
	buf.WriteString("}\n\n")
}

func (g *generator) omitZeroField(s schema) bool {
	if s.Ref != "" {
		def := g.mustDef(refSchemaName(s.Ref))
		return len(def.Properties) > 0 && len(def.OneOf) == 0 && len(def.AnyOf) == 0 && len(def.Enum) == 0
	}
	if len(s.AllOf) == 1 {
		return g.omitZeroField(s.AllOf[0])
	}
	return false
}

func (g *generator) goType(s schema, topLevel bool) (string, bool) {
	if typ, nullable, ok := nullableTypeUnion(s); ok {
		s.Type = typ
		goType, nestedNullable := g.goType(s, topLevel)
		if nestedNullable {
			panic("nested nullable type union")
		}
		return pointerType(goType), nullable
	}
	if s.Ref != "" {
		return refGoName(s.Ref), false
	}
	if len(s.AllOf) > 0 {
		if len(s.AllOf) != 1 {
			panic("allOf with more than one entry")
		}
		return g.goType(s.AllOf[0], topLevel)
	}
	if len(s.OneOf) > 0 {
		return rawMessageType, false
	}
	if s.Const != nil {
		return constGoType(s.Const), false
	}
	if len(s.AnyOf) > 0 {
		return g.anyOfType(s, topLevel)
	}
	switch schemaType(s) {
	case "boolean":
		return "bool", false
	case "string":
		return "string", false
	case "integer":
		if s.Format == "int64" {
			return "int64", false
		}
		if s.Format == "uint16" {
			return "ProtocolVersion", false
		}
		if s.Format == "int32" {
			return "int32", false
		}
		if s.Format == "uint32" {
			return "uint32", false
		}
		if s.Format == "uint64" {
			return "uint64", false
		}
		panicf("unsupported integer schema format %q", s.Format)
	case "number":
		if s.Format == "double" {
			return "float64", false
		}
		panicf("unsupported number schema format %q", s.Format)
	case "array":
		if s.Items == nil {
			panic("array without items")
		}
		itemType, nullable := g.goType(*s.Items, false)
		if nullable {
			panic("nullable array items are unsupported")
		}
		return "[]" + itemType, false
	case "object":
		if additionalPropertiesTrue(s) && len(s.Properties) == 0 {
			return "map[string]any", false
		}
		if len(s.Properties) == 0 {
			return "map[string]any", false
		}
		return rawMessageType, false
	case "null":
		return "", true
	case "":
		return rawMessageType, false
	default:
		panicf("unsupported schema type %q", schemaType(s))
	}
	return "", false
}

func (g *generator) anyOfType(s schema, topLevel bool) (string, bool) {
	if len(s.AnyOf) == 1 {
		return g.goType(s.AnyOf[0], topLevel)
	}
	if len(s.AnyOf) > 2 {
		if allConstSamePrimitive(s.AnyOf) {
			return constPrimitiveType(s.AnyOf), false
		}
		return rawMessageType, false
	}
	firstType, firstNull := g.goType(s.AnyOf[0], topLevel)
	secondType, secondNull := g.goType(s.AnyOf[1], topLevel)
	if !firstNull && !secondNull {
		return rawMessageType, false
	}
	if firstNull == secondNull {
		panic("anyOf must contain exactly one null variant")
	}
	if firstNull {
		return pointerType(secondType), true
	}
	return pointerType(firstType), true
}

func allConstStrings(values []schema) bool {
	if len(values) == 0 {
		return false
	}
	for _, value := range values {
		if _, ok := value.Const.(string); !ok {
			return false
		}
	}
	return true
}

func constValues(values []schema) []any {
	out := make([]any, 0, len(values))
	for _, value := range values {
		out = append(out, value.Const)
	}
	return out
}

func discriminatorConst(s schema, prop string) (string, bool) {
	propSchema, ok := s.Properties[prop]
	if !ok {
		return "", false
	}
	value, ok := propSchema.Const.(string)
	return value, ok && value != ""
}

func allConstSamePrimitive(values []schema) bool {
	if len(values) == 0 {
		return false
	}
	base := ""
	for _, value := range values {
		if value.Const == nil {
			continue
		}
		typ := constGoType(value.Const)
		if base == "" {
			base = typ
		} else if base != typ {
			return false
		}
	}
	return base != ""
}

func constPrimitiveType(values []schema) string {
	for _, value := range values {
		if value.Const != nil {
			return constGoType(value.Const)
		}
	}
	panic("no const primitive type")
}

func constGoType(value any) string {
	switch value.(type) {
	case string:
		return "string"
	case float64:
		return "int32"
	default:
		panicf("unsupported const type %T", value)
	}
	return ""
}

func constLiteral(value any) string {
	switch value := value.(type) {
	case string:
		return fmt.Sprintf("%q", value)
	case float64:
		return fmt.Sprintf("%d", int(value))
	default:
		panicf("unsupported const literal %T", value)
	}
	return ""
}

func constName(value any) string {
	switch value := value.(type) {
	case string:
		return fieldName(value)
	case float64:
		if value < 0 {
			return fmt.Sprintf("Minus%d", -int(value))
		}
		return fmt.Sprintf("%d", int(value))
	default:
		panicf("unsupported const name %T", value)
	}
	return ""
}

func schemaType(s schema) string {
	switch t := s.Type.(type) {
	case string:
		return t
	case []any:
		if len(t) == 2 && t[0] == "object" && t[1] == "null" && additionalPropertiesTrue(s) {
			return "object"
		}
		panicf("unsupported type union %v", t)
	case nil:
		return ""
	default:
		panicf("unsupported type value %T", s.Type)
	}
	return ""
}

func nullableTypeUnion(s schema) (string, bool, bool) {
	types, ok := s.Type.([]any)
	if !ok {
		return "", false, false
	}
	if len(types) != 2 {
		panicf("unsupported type union %v", types)
	}
	var typ string
	for _, raw := range types {
		value, ok := raw.(string)
		if !ok {
			panicf("unsupported type union member %T", raw)
		}
		if value == "null" {
			continue
		}
		if typ != "" {
			panicf("unsupported type union %v", types)
		}
		typ = value
	}
	if typ == "" {
		panicf("unsupported type union %v", types)
	}
	return typ, true, true
}

func additionalPropertiesTrue(s schema) bool {
	b, ok := s.AdditionalProperties.(bool)
	return ok && b
}

func pointerType(typ string) string {
	if strings.HasPrefix(typ, "*") || strings.HasPrefix(typ, "[]") || strings.HasPrefix(typ, "map[") {
		return typ
	}
	return "*" + typ
}

func refSchemaName(ref string) string {
	const prefix = "#/$defs/"
	if !strings.HasPrefix(ref, prefix) {
		panicf("unsupported ref %q", ref)
	}
	return strings.TrimPrefix(ref, prefix)
}

func refGoName(ref string) string {
	return goTypeName(refSchemaName(ref))
}

func goTypeName(name string) string {
	name = strings.ReplaceAll(name, "Mcp", "MCP")
	name = strings.ReplaceAll(name, "Id", "ID")
	name = strings.ReplaceAll(name, "Uri", "URI")
	return name
}

func fieldName(name string) string {
	if name == "_meta" {
		return "Meta"
	}
	parts := strings.FieldsFunc(name, func(r rune) bool { return r == '_' || r == '-' })
	for i, part := range parts {
		parts[i] = goNamePart(part)
	}
	return goTypeName(strings.Join(parts, ""))
}

func goNamePart(s string) string {
	switch strings.ToLower(s) {
	case "fs":
		return "FS"
	case "http":
		return "HTTP"
	case "id":
		return "ID"
	case "mcp":
		return "MCP"
	case "sse":
		return "SSE"
	case "uri":
		return "URI"
	case "url":
		return "URL"
	}
	return upperFirst(s)
}

func upperFirst(s string) string {
	if s == "" {
		return s
	}
	runes := []rune(s)
	runes[0] = unicode.ToUpper(runes[0])
	return string(runes)
}

func panicf(format string, args ...any) {
	panic(fmt.Sprintf(format, args...))
}
