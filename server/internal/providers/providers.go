// Icon represents an icon with theme support
type Icon struct {
	Src        string   `json:"src"`
	MimeType   string   `json:"mimeType,omitempty"`
	Sizes      []string `json:"sizes,omitempty"`
	Theme      string   `json:"theme,omitempty"`      // "light" or "dark"
	InvertDark bool     `json:"invertDark,omitempty"` // If true, invert colors for dark mode
}

// ProviderCategory classifies what a credential is used for.
type ProviderCategory string

const (
	// CategoryLLM is for AI model providers (API keys / OAuth tokens sent to LLM APIs).
	CategoryLLM ProviderCategory = "llm"
	// CategoryVCS is for version-control credentials (git clone, push, PRs).
	CategoryVCS ProviderCategory = "vcs"
	// CategorySandboxProvider is for sandbox infrastructure provider credentials.
	CategorySandboxProvider ProviderCategory = "sandbox-provider"
)

// AuthProvider represents an auth provider option
type AuthProvider struct {
	ID                      string           `json:"id"`
	Name                    string           `json:"name"`
	ConfiguredName          string           `json:"configuredName,omitempty"`
	Description             string           `json:"description,omitempty"`
	SecretLabel             string           `json:"secretLabel,omitempty"`
	SecretDescription       string           `json:"secretDescription,omitempty"`
	AutoGenerateSecret      bool             `json:"autoGenerateSecret,omitempty"`
	AutoGeneratePrefix      string           `json:"autoGeneratePrefix,omitempty"`
	AutoGenerateDescription string           `json:"autoGenerateDescription,omitempty"`
	Icons                   []Icon           `json:"icons,omitempty"`
	Env                     []string         `json:"env,omitempty"` // Environment variable names for API keys
	Category                ProviderCategory `json:"category"`
}

