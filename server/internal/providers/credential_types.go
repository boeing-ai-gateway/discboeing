// GetCredentialTypes returns the credential choices used by the current UI.
func GetCredentialTypes() []CredentialType {
	loadProviders()

	result := make([]CredentialType, 0, len(credentialTypeSpecs))
	for _, spec := range credentialTypeSpecs {
		displayProvider := Get(spec.DisplayProviderID)
		if displayProvider == nil {
			continue
		}

		envProviderID := spec.EnvProviderID
		if envProviderID == "" {
			envProviderID = spec.BackendProviderID
		}
		envProvider := Get(envProviderID)

		ct := CredentialType{
			ID:                      spec.ID,
			Provider:                spec.DisplayProviderID,
			BackendProvider:         spec.BackendProviderID,
			Name:                    displayProvider.Name,
			ConfiguredName:          displayProvider.ConfiguredName,
			Description:             displayProvider.Description,
			SecretLabel:             displayProvider.SecretLabel,
			SecretDescription:       displayProvider.SecretDescription,
			AutoGenerateSecret:      displayProvider.AutoGenerateSecret,
			AutoGeneratePrefix:      displayProvider.AutoGeneratePrefix,
			AutoGenerateDescription: displayProvider.AutoGenerateDescription,
			Icons:                   append([]Icon(nil), displayProvider.Icons...),
			Category:                displayProvider.Category,
			Group:                   spec.Group,
			GroupName:               spec.GroupName,
			AuthType:                spec.AuthType,
			OAuth:                   spec.OAuth,
		}
		if envProvider != nil {
			ct.Env = append([]string(nil), envProvider.Env...)
		}
		result = append(result, ct)
	}

	return result
}
