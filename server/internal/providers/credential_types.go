package providers

// CredentialTypeGroup groups credential choices in the UI.
type CredentialTypeGroup string

const (
	CredentialTypeGroupModelProviders CredentialTypeGroup = "model-providers"
	CredentialTypeGroupGitVersion     CredentialTypeGroup = "git-version-control"
	CredentialTypeGroupTools          CredentialTypeGroup = "tools"
)

// OAuthKind describes the interaction style for an OAuth flow.
type OAuthKind string

const (
	OAuthKindAuthorizationCode OAuthKind = "authorization_code"
	OAuthKindDeviceCode        OAuthKind = "device_code"
)

// OAuthConfig describes how the UI should drive an OAuth credential flow.
type OAuthConfig struct {
	Provider          string    `json:"provider"`
	Kind              OAuthKind `json:"kind"`
	Description       string    `json:"description,omitempty"`
	InputLabel        string    `json:"inputLabel,omitempty"`
	InputPlaceholder  string    `json:"inputPlaceholder,omitempty"`
	AllowsDirectToken bool      `json:"allowsDirectToken,omitempty"`
}

// CredentialType describes a selectable credential option in the current UI.
type CredentialType struct {
	ID                      string              `json:"id"`
	Provider                string              `json:"provider"`
	BackendProvider         string              `json:"backendProvider"`
	Name                    string              `json:"name"`
	ConfiguredName          string              `json:"configuredName,omitempty"`
	Description             string              `json:"description,omitempty"`
	SecretLabel             string              `json:"secretLabel,omitempty"`
	SecretDescription       string              `json:"secretDescription,omitempty"`
	AutoGenerateSecret      bool                `json:"autoGenerateSecret,omitempty"`
	AutoGeneratePrefix      string              `json:"autoGeneratePrefix,omitempty"`
	AutoGenerateDescription string              `json:"autoGenerateDescription,omitempty"`
	Icons                   []Icon              `json:"icons,omitempty"`
	Env                     []string            `json:"env,omitempty"`
	Category                ProviderCategory    `json:"category"`
	Group                   CredentialTypeGroup `json:"group"`
	GroupName               string              `json:"groupName"`
	AuthType                string              `json:"authType"`
	OAuth                   *OAuthConfig        `json:"oauth,omitempty"`
}

