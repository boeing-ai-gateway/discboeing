//go:build ignore

package main

import (
	"bytes"
	"encoding/json"
	"flag"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"
	"unicode"
)

type schemaFile struct {
	Defs map[string]*schema `json:"$defs"`
}

type schema struct {
	Ref                string             `json:"$ref"`
	Title              string             `json:"title"`
	Description        string             `json:"description"`
	Type               string             `json:"type"`
	Format             string             `json:"format"`
	ContentEncoding    string             `json:"contentEncoding"`
	Enum               []string           `json:"enum"`
	Required           []string           `json:"required"`
	Properties         map[string]*schema `json:"properties"`
	Items              *schema            `json:"items"`
	AdditionalProperty any                `json:"additionalProperties"`
}

type generator struct {
	pkg     string
	defs    map[string]*schema
	imports map[string]bool
}

func main() {
	schemaPath := flag.String("schema", "types.schema.json", "JSON Schema input path")
	outPath := flag.String("out", "types.gen.go", "Go output path")
	pkg := flag.String("package", "api", "Go package name")
	flag.Parse()

	data, err := os.ReadFile(*schemaPath)
	if err != nil {
		fatal(err)
	}
	var sf schemaFile
	if err := json.Unmarshal(data, &sf); err != nil {
		fatal(err)
	}
	if len(sf.Defs) == 0 {
		fatal(fmt.Errorf("schema has no $defs"))
	}

	g := &generator{pkg: *pkg, defs: sf.Defs, imports: map[string]bool{}}
	src, err := g.generate(filepath.Base(*schemaPath))
	if err != nil {
		fatal(err)
	}
	formatted, err := format.Source(src)
	if err != nil {
		_, _ = os.Stderr.Write(src)
		fatal(err)
	}
	if err := os.WriteFile(*outPath, formatted, 0o644); err != nil {
		fatal(err)
	}
}

func fatal(err error) {
	_, _ = fmt.Fprintln(os.Stderr, err)
	os.Exit(1)
}

func (g *generator) generate(schemaName string) ([]byte, error) {
	names := make([]string, 0, len(g.defs))
	for name := range g.defs {
		names = append(names, name)
	}
	sort.Strings(names)

	var body bytes.Buffer
	for _, name := range names {
		def := g.defs[name]
		if len(def.Enum) > 0 {
			g.writeEnum(&body, name, def)
			continue
		}
		if def.Type == "object" && len(def.Properties) > 0 && name != "JSONRawMessage" {
			g.writeStruct(&body, name, def)
			continue
		}
		g.writeAlias(&body, name, def)
	}

	var out bytes.Buffer
	fmt.Fprintf(&out, "// Code generated by go generate from %s; DO NOT EDIT.\n\n", schemaName)
	fmt.Fprintf(&out, "package %s\n\n", g.pkg)
	if len(g.imports) > 0 {
		imports := make([]string, 0, len(g.imports))
		for imp := range g.imports {
			imports = append(imports, imp)
		}
		sort.Strings(imports)
		out.WriteString("import (\n")
		for _, imp := range imports {
			fmt.Fprintf(&out, "\t%q\n", imp)
		}
		out.WriteString(")\n\n")
	}
	out.Write(body.Bytes())
	return out.Bytes(), nil
}

func (g *generator) writeAlias(out *bytes.Buffer, name string, s *schema) {
	goType := g.aliasType(name, s)
	writeComment(out, name, descriptionFor(name, s, "is a Meta API type generated from JSON Schema."))
	fmt.Fprintf(out, "type %s %s\n\n", name, goType)
}

func (g *generator) aliasType(name string, s *schema) string {
	switch name {
	case "Timestamp":
		g.imports["time"] = true
		return "time.Time"
	case "Base64Bytes":
		return "[]byte"
	case "JSONRawMessage":
		g.imports["encoding/json"] = true
		return "json.RawMessage"
	}
	if s.Type == "array" && s.Items != nil {
		return "[]" + g.goType(s.Items, true)
	}
	return g.goType(s, true)
}

func (g *generator) writeEnum(out *bytes.Buffer, name string, s *schema) {
	writeComment(out, name, descriptionFor(name, s, "is a Meta API string enum."))
	fmt.Fprintf(out, "type %s string\n\n", name)
	out.WriteString("const (\n")
	for _, value := range s.Enum {
		constName := name + enumSuffix(value)
		fmt.Fprintf(out, "\t%s %s = %q\n", constName, name, value)
	}
	out.WriteString(")\n\n")
}

func (g *generator) writeStruct(out *bytes.Buffer, name string, s *schema) {
	writeComment(out, name, descriptionFor(name, s, "is a Meta API object generated from JSON Schema."))
	fmt.Fprintf(out, "type %s struct {\n", name)
	required := map[string]bool{}
	for _, field := range s.Required {
		required[field] = true
	}
	props := make([]string, 0, len(s.Properties))
	for prop := range s.Properties {
		props = append(props, prop)
	}
	sort.Strings(props)
	for _, prop := range props {
		ps := s.Properties[prop]
		fieldName := exportedName(prop)
		goType := g.goType(ps, required[prop])
		jsonTag := prop
		if !required[prop] {
			jsonTag += ",omitempty"
		}
		if desc := strings.TrimSpace(ps.Description); desc != "" {
			writeComment(out, fieldName, desc)
		}
		fmt.Fprintf(out, "\t%s %s `json:%q`\n", fieldName, goType, jsonTag)
	}
	out.WriteString("}\n\n")
}

func (g *generator) goType(s *schema, required bool) string {
	if s == nil {
		return "any"
	}
	if s.Ref != "" {
		name := refName(s.Ref)
		if name == "JSONRawMessage" {
			g.imports["encoding/json"] = true
			return "json.RawMessage"
		}
		if !required && isPointerRef(name) {
			return "*" + name
		}
		return name
	}
	switch s.Type {
	case "string":
		if s.Format == "date-time" {
			g.imports["time"] = true
			if required {
				return "time.Time"
			}
			return "*time.Time"
		}
		if s.ContentEncoding == "base64" {
			return "[]byte"
		}
		if required {
			return "string"
		}
		return "*string"
	case "boolean":
		if required {
			return "bool"
		}
		return "*bool"
	case "integer":
		if required {
			return "int"
		}
		return "*int"
	case "number":
		if required {
			return "float64"
		}
		return "*float64"
	case "array":
		return "[]" + g.goType(s.Items, true)
	case "object":
		g.imports["encoding/json"] = true
		return "json.RawMessage"
	default:
		return "any"
	}
}

func isPointerRef(name string) bool {
	switch name {
	case "Base64Bytes", "JSONRawMessage":
		return false
	default:
		return true
	}
}

func descriptionFor(name string, s *schema, fallback string) string {
	if s.Description != "" {
		return s.Description
	}
	if s.Title != "" {
		return s.Title
	}
	return name + " " + fallback
}

func refName(ref string) string {
	const prefix = "#/$defs/"
	if strings.HasPrefix(ref, prefix) {
		return strings.TrimPrefix(ref, prefix)
	}
	parts := strings.Split(ref, "/")
	return parts[len(parts)-1]
}

func writeComment(out *bytes.Buffer, name, text string) {
	text = strings.TrimSpace(strings.ReplaceAll(text, "\n", " "))
	if text == "" {
		text = name + " is generated from JSON Schema."
	}
	if !strings.HasPrefix(text, name) {
		text = name + " " + strings.ToLower(text[:1]) + text[1:]
	}
	for _, line := range wrap(text, 88) {
		fmt.Fprintf(out, "\t// %s\n", line)
	}
}

func wrap(s string, width int) []string {
	words := strings.Fields(s)
	if len(words) == 0 {
		return nil
	}
	var lines []string
	line := words[0]
	for _, word := range words[1:] {
		if len(line)+1+len(word) > width {
			lines = append(lines, line)
			line = word
			continue
		}
		line += " " + word
	}
	return append(lines, line)
}

var splitRegexp = regexp.MustCompile(`[._\-\s:]+`)

func exportedName(s string) string {
	parts := identifierWords(s)
	for i, part := range parts {
		parts[i] = wordName(part)
	}
	return strings.Join(parts, "")
}

func enumSuffix(s string) string {
	return exportedName(s)
}

func identifierWords(s string) []string {
	raw := splitRegexp.Split(s, -1)
	words := make([]string, 0, len(raw))
	for _, part := range raw {
		if part == "" {
			continue
		}
		words = append(words, splitCamel(part)...)
	}
	return words
}

func splitCamel(s string) []string {
	var words []string
	runes := []rune(s)
	start := 0
	for i := 1; i < len(runes); i++ {
		prev := runes[i-1]
		cur := runes[i]
		var next rune
		if i+1 < len(runes) {
			next = runes[i+1]
		}
		if unicode.IsLower(prev) && unicode.IsUpper(cur) || unicode.IsUpper(prev) && unicode.IsUpper(cur) && next != 0 && unicode.IsLower(next) {
			words = append(words, string(runes[start:i]))
			start = i
		}
	}
	words = append(words, string(runes[start:]))
	return words
}

func wordName(s string) string {
	if s == "" {
		return ""
	}
	upper := strings.ToUpper(s)
	switch upper {
	case "ID", "URL", "URI", "API", "JSON", "JWT", "OIDC", "OAUTH", "LLM", "CEK", "KMS", "HSM", "JWKS", "HTTP", "IP":
		if upper == "OAUTH" {
			return "OAuth"
		}
		return upper
	}
	runes := []rune(strings.ToLower(s))
	runes[0] = unicode.ToUpper(runes[0])
	return string(runes)
}
