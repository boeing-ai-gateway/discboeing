//go:build ignore

package main

import (
	"bytes"
	"context"
	"flag"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"
	"unicode"

	"github.com/getkin/kin-openapi/openapi3"
	"gopkg.in/yaml.v3"
)

type openAPI struct {
	Paths map[string]map[string]operation `yaml:"paths"`
}

type operation struct {
	Tags           []string     `yaml:"tags"`
	OperationID    string       `yaml:"operationId"`
	DiscobotAction string       `yaml:"x-discobot-action"`
	Summary        string       `yaml:"summary"`
	Description    string       `yaml:"description"`
	Parameters     []parameter  `yaml:"parameters"`
	RequestBody    *requestBody `yaml:"requestBody"`
}

type parameter struct {
	Ref         string `yaml:"$ref"`
	Name        string `yaml:"name"`
	In          string `yaml:"in"`
	Required    bool   `yaml:"required"`
	Description string `yaml:"description"`
}

type requestBody struct {
	Required bool `yaml:"required"`
}

type route struct {
	Method      string
	Path        string
	OperationID string
	Action      string
	Group       string
	Description string
	Params      []parameter
	HasBody     bool
}

func main() {
	openapiPath := flag.String("openapi", "../../api/openapi.yaml", "OpenAPI YAML path")
	outPath := flag.String("out", "routes.gen.go", "generated output path")
	authzOutPath := flag.String("authz-out", "../authz/actions.gen.go", "generated authz actions output path")
	handlersDir := flag.String("handlers-dir", "../handlers", "handlers package directory")
	flag.Parse()

	data, err := os.ReadFile(*openapiPath)
	if err != nil {
		fatal(err)
	}
	if err := validateOpenAPI(data); err != nil {
		fatal(err)
	}
	var doc openAPI
	if err := yaml.Unmarshal(data, &doc); err != nil {
		fatal(err)
	}

	routes := collectRoutes(doc)
	src, err := generate(data, routes)
	if err != nil {
		fatal(err)
	}
	formatted, err := format.Source(src)
	if err != nil {
		_, _ = os.Stderr.Write(src)
		fatal(err)
	}
	if err := os.WriteFile(*outPath, formatted, 0o644); err != nil {
		fatal(err)
	}

	authzSrc, err := generateAuthz(routes)
	if err != nil {
		fatal(err)
	}
	authzFormatted, err := format.Source(authzSrc)
	if err != nil {
		_, _ = os.Stderr.Write(authzSrc)
		fatal(err)
	}
	if err := os.WriteFile(*authzOutPath, authzFormatted, 0o644); err != nil {
		fatal(err)
	}
	if err := ensureHandlerStubs(*handlersDir, routes); err != nil {
		fatal(err)
	}
}

func fatal(err error) {
	_, _ = fmt.Fprintln(os.Stderr, err)
	os.Exit(1)
}

func validateOpenAPI(data []byte) error {
	loader := openapi3.NewLoader()
	doc, err := loader.LoadFromData(data)
	if err != nil {
		return fmt.Errorf("load OpenAPI: %w", err)
	}
	if err := doc.Validate(context.Background()); err != nil {
		return fmt.Errorf("validate OpenAPI: %w", err)
	}
	return nil
}

func collectRoutes(doc openAPI) []route {
	methods := map[string]string{
		"get":     "GET",
		"post":    "POST",
		"put":     "PUT",
		"patch":   "PATCH",
		"delete":  "DELETE",
		"head":    "HEAD",
		"options": "OPTIONS",
	}
	var routes []route
	for path, ops := range doc.Paths {
		for methodLower, op := range ops {
			method, ok := methods[strings.ToLower(methodLower)]
			if !ok || op.OperationID == "" {
				continue
			}
			group := "Default"
			if len(op.Tags) > 0 {
				group = op.Tags[0]
			}
			desc := strings.TrimSpace(op.Description)
			if desc == "" {
				desc = strings.TrimSpace(op.Summary)
			}
			action := strings.TrimSpace(op.DiscobotAction)
			if action == "" {
				fatal(fmt.Errorf("missing x-discobot-action for operation %q", op.OperationID))
			}
			routes = append(routes, route{
				Method:      method,
				Path:        path,
				OperationID: op.OperationID,
				Action:      action,
				Group:       group,
				Description: desc,
				Params:      op.Parameters,
				HasBody:     op.RequestBody != nil,
			})
		}
	}
	sort.Slice(routes, func(i, j int) bool {
		if routes[i].Path == routes[j].Path {
			return routes[i].Method < routes[j].Method
		}
		return routes[i].Path < routes[j].Path
	})
	return routes
}

func generate(openapi []byte, routes []route) ([]byte, error) {
	var out bytes.Buffer
	out.WriteString("// Code generated by go generate from meta/api/openapi.yaml; DO NOT EDIT.\n\n")
	out.WriteString("package routes\n\n")
	out.WriteString("import \"github.com/obot-platform/discobot/meta/internal/handlers\"\n\n")
	fmt.Fprintf(&out, "const openAPIYAML = %q\n\n", string(openapi))
	out.WriteString("// OpenAPIYAML returns the generated OpenAPI document.\n")
	out.WriteString("func OpenAPIYAML() string { return openAPIYAML }\n\n")
	out.WriteString("// GeneratedRoutes returns route metadata generated from the OpenAPI document.\n")
	out.WriteString("func GeneratedRoutes(h *handlers.Handlers) []Route {\n")
	out.WriteString("\treturn []Route{\n")
	for _, route := range routes {
		fmt.Fprintf(&out, "\t\t{\n")
		fmt.Fprintf(&out, "\t\t\tMethod: %q,\n", route.Method)
		fmt.Fprintf(&out, "\t\t\tPattern: %q,\n", route.Path)
		fmt.Fprintf(&out, "\t\t\tOperationID: %q,\n", route.OperationID)
		fmt.Fprintf(&out, "\t\t\tHandler: h.%s,\n", operationMethodName(route.OperationID))
		fmt.Fprintf(&out, "\t\t\tMeta: Meta{\n")
		fmt.Fprintf(&out, "\t\t\t\tGroup: %q,\n", route.Group)
		fmt.Fprintf(&out, "\t\t\t\tDescription: %q,\n", route.Description)
		if len(route.Params) > 0 {
			out.WriteString("\t\t\t\tParams: []Param{\n")
			for _, param := range route.Params {
				if param.Ref != "" {
					continue
				}
				fmt.Fprintf(&out, "\t\t\t\t\t{Name: %q, In: %q, Required: %t},\n", param.Name, param.In, param.Required)
			}
			out.WriteString("\t\t\t\t},\n")
		}
		if route.HasBody {
			fmt.Fprintf(&out, "\t\t\t\tBody: bodyExample(%q),\n", route.Method)
		}
		out.WriteString("\t\t\t},\n")
		out.WriteString("\t\t},\n")
	}
	out.WriteString("\t}\n")
	out.WriteString("}\n")
	return out.Bytes(), nil
}

func generateAuthz(routes []route) ([]byte, error) {
	var out bytes.Buffer
	out.WriteString("// Code generated by go generate from meta/api/openapi.yaml; DO NOT EDIT.\n\n")
	out.WriteString("package authz\n\n")
	actions := map[string]bool{}
	for _, route := range routes {
		actions[route.Action] = true
	}
	ordered := make([]string, 0, len(actions))
	for action := range actions {
		ordered = append(ordered, action)
	}
	sort.Strings(ordered)

	out.WriteString("const (\n")
	for _, action := range ordered {
		fmt.Fprintf(&out, "\t%s Action = %q\n", actionConstName(action), action)
	}
	out.WriteString(")\n\n")

	out.WriteString("var operationActions = map[string]Action{\n")
	for _, route := range routes {
		fmt.Fprintf(&out, "\t%q: %s,\n", route.OperationID, actionConstName(route.Action))
	}
	out.WriteString("}\n")
	return out.Bytes(), nil
}

func actionConstName(action string) string {
	var b strings.Builder
	b.WriteString("Action")
	for _, part := range strings.FieldsFunc(action, func(r rune) bool {
		return r == '_' || r == '.' || r == '-' || r == ' '
	}) {
		if part == "" {
			continue
		}
		if initialism, ok := actionInitialisms[part]; ok {
			b.WriteString(initialism)
			continue
		}
		runes := []rune(part)
		b.WriteRune(unicode.ToUpper(runes[0]))
		b.WriteString(string(runes[1:]))
	}
	return b.String()
}

func operationMethodName(operationID string) string {
	parts := splitIdentifier(operationID)
	var b strings.Builder
	for _, part := range parts {
		lower := strings.ToLower(part)
		if initialism, ok := operationInitialisms[lower]; ok {
			b.WriteString(initialism)
			continue
		}
		runes := []rune(part)
		if len(runes) == 0 {
			continue
		}
		b.WriteRune(unicode.ToUpper(runes[0]))
		if len(runes) > 1 {
			b.WriteString(string(runes[1:]))
		}
	}
	return b.String()
}

func splitIdentifier(value string) []string {
	var parts []string
	var current []rune
	for i, r := range value {
		if r == '_' || r == '-' || r == ' ' {
			if len(current) > 0 {
				parts = append(parts, string(current))
				current = nil
			}
			continue
		}
		if i > 0 && unicode.IsUpper(r) && len(current) > 0 {
			parts = append(parts, string(current))
			current = nil
		}
		current = append(current, r)
	}
	if len(current) > 0 {
		parts = append(parts, string(current))
	}
	return parts
}

func ensureHandlerStubs(dir string, routes []route) error {
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return err
	}
	existing, err := existingHandlerMethods(dir)
	if err != nil {
		return err
	}
	missing := map[string][]route{}
	for _, route := range routes {
		method := operationMethodName(route.OperationID)
		if existing[method] {
			continue
		}
		missing[handlerGroupFile(route.Group)] = append(missing[handlerGroupFile(route.Group)], route)
	}
	for fileName, routes := range missing {
		path := filepath.Join(dir, fileName)
		if err := appendHandlerStubs(path, routes); err != nil {
			return err
		}
	}
	return nil
}

func existingHandlerMethods(dir string) (map[string]bool, error) {
	result := map[string]bool{}
	files, err := filepath.Glob(filepath.Join(dir, "*.go"))
	if err != nil {
		return nil, err
	}
	methodRE := regexp.MustCompile(`func\s*\(\s*\w+\s+\*Handlers\s*\)\s+([A-Z]\w*)\s*\(`)
	for _, file := range files {
		data, err := os.ReadFile(file)
		if err != nil {
			return nil, err
		}
		for _, match := range methodRE.FindAllSubmatch(data, -1) {
			result[string(match[1])] = true
		}
	}
	return result, nil
}

func appendHandlerStubs(path string, routes []route) error {
	sort.Slice(routes, func(i, j int) bool {
		return routes[i].OperationID < routes[j].OperationID
	})
	var out bytes.Buffer
	newFile := false
	if _, err := os.Stat(path); os.IsNotExist(err) {
		newFile = true
		out.WriteString("package handlers\n\n")
		out.WriteString("import \"net/http\"\n")
	} else if err != nil {
		return err
	}
	for _, route := range routes {
		fmt.Fprintf(&out, "\nfunc (h *Handlers) %s(w http.ResponseWriter, r *http.Request) {\n", operationMethodName(route.OperationID))
		fmt.Fprintf(&out, "\th.NotImplemented(%q, w, r)\n", route.OperationID)
		out.WriteString("}\n")
	}
	content := out.Bytes()
	if newFile {
		formatted, err := format.Source(content)
		if err != nil {
			return err
		}
		content = formatted
	}
	f, err := os.OpenFile(path, os.O_CREATE|os.O_APPEND|os.O_WRONLY, 0o644)
	if err != nil {
		return err
	}
	defer f.Close()
	_, err = f.Write(content)
	return err
}

func handlerGroupFile(group string) string {
	group = strings.ToLower(group)
	group = strings.ReplaceAll(group, " ", "_")
	group = strings.ReplaceAll(group, "-", "_")
	return group + ".go"
}

var actionInitialisms = map[string]string{
	"id":       "ID",
	"jwks":     "JWKS",
	"oidc":     "OIDC",
	"oauth":    "OAuth",
	"userinfo": "UserInfo",
}

var operationInitialisms = map[string]string{
	"id":    "ID",
	"jwks":  "JWKS",
	"oauth": "OAuth",
}
